    private void loadTable() {
        try {
            String sql = "SELECT Product_ID, Product_Name,Category_Name,Price, Stocks FROM tblproducts WHERE Product_ID";
            ps = connection.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            DefaultTableModel model = new DefaultTableModel();
            tblProduct.setModel(model);

            model.addColumn("Product ID");
            model.addColumn("Product Name");
            model.addColumn("Category_Name");
            model.addColumn("Unit Price");
            model.addColumn("Stocks");

            if (rs.next()) {
                String productId = rs.getString("Product_ID");
                String productName = rs.getString("Product_Name");
                String categoryName = rs.getString("Category_Name");
                double Price = rs.getDouble("Price");
                int stocks = rs.getInt("Stocks");

                model.addRow(new Object[]{productId, productName, categoryName, Price, stocks});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

