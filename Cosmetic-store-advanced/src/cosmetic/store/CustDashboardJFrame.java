    private void jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActionPerformed
       try {
    // Generate the next order ID automatically
    String getMaxIdSql = "SELECT MAX(CAST(SUBSTR(OrderID, 2) AS UNSIGNED)) AS max_id FROM tblorder";
    ps = connection.prepareStatement(getMaxIdSql);
    ResultSet rs = ps.executeQuery();

    String newId;
    if (rs.next() && rs.getObject("max_id") != null) {
        int maxId = rs.getInt("max_id");
        newId = "O" + String.format("%04d", maxId + 1);  // Format new ID with leading zeros
    } else {
        newId = "O0001";  // If no IDs exist, start with "O0001"
    }

    String id = txtID.getText();
    String name = txtCustName.getText();
    String Productid = txtProductId.getText();
    String Productname = txtProductName.getText();
    Double Uprice = Double.parseDouble(txtPrice.getText());
    int Qty = Integer.parseInt(txtii.getText());
    Double pfinal = Uprice * Qty;

    reduceStock(Productid, Qty);

    // Update the SQL query to include the current date and time
    String sql = "INSERT INTO tblorder (OrderId, CustID, CustName, ProID, Product_Name, UPrice, Qty, Price, OrderDate) " +
                 "VALUES (?, ?, ?, ?, ?, ?, ?, ?, CURRENT_TIMESTAMP())";
    ps = connection.prepareStatement(sql);
    ps.setString(1, newId);
    ps.setString(2, id);
    ps.setString(3, name);
    ps.setString(4, Productid);
    ps.setString(5, Productname);
    ps.setDouble(6, Uprice);
    ps.setInt(7, Qty);
    ps.setDouble(8, pfinal);

    if (ps.executeUpdate() > 0) {
        int res = JOptionPane.showConfirmDialog(this,
                "Do you want to Place Order ?",
                "Place Order",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);

        if (res == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, "Order Record Inserted!");
            clear();
            productDB.loadProducts((DefaultTableModel) tblProduct.getModel());
            loadTable1();
        }
    }
} catch (Exception e) {
    e.printStackTrace();
}
    }//GEN-LAST:event_jButtonActionPerformed

